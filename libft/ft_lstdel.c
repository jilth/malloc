#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*list;
	t_list	*list_next;

	if ((*alst)->next != NULL)
		list = (*alst);
	while (list_next != NULL)
	{
		list_next = list->next;
		ft_lstdelone(&list, del);
		list = list_next;
	}
	*alst = NULL;
}
