#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int	i;

	i = 0;
	while (s1[i] == s2[i])
	{
		if (s1[i] == '\0')
			return (0);
		i++;
	}
	i = (int)(s1[i] - s2[i]) > 0 ? 1 : - 1;
	return (i);
}
